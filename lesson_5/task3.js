// 3.1 FOR

let number1 = 7;

for (let i = 0; i <= 10; i++) {
    let mutliplication = number1 * i;
    console.log(number1 + " x " + i + " = " + mutliplication);
}

// 3.2 WHILE
console.log("------------------------");

let number2 = 7;
let i = 0;

while (i <= 10) {
    let mutliplication = number2 * i;
    console.log(number2 + " x " + i + " = " + mutliplication);
    i++;
}